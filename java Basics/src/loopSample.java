public class loopSample {
    public static void main(String[] args) {
        //do-while loop
        int i=5;

        do{
            System.out.println(i);
            i--;
        }
        while(i>0);

        //for loop

        int j=10;
        for(j=10;j>0;j--){
            System.out.println(j);
        }
    }
}
